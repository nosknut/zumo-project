#ifndef TIMER_h
#define TIMER_h

struct Timer
{
    unsigned long startTime = 0;

    void reset()
    {
        startTime = millis();
    }

    unsigned long getElapsedTime()
    {
        return millis() - startTime;
    }

    bool isFinished(unsigned long duration)
    {
        return getElapsedTime() >= duration;
    }
};

#endif
